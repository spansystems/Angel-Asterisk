package com.angel.agent;

import org.asteriskjava.live.AsteriskChannel;

/**
 * General class for the user information
 * 
 */
public class User
{

	private String callerId;
	private String parkingLotNo;
	private String channelId;
	private AsteriskChannel channel;
	private String channelName;

	public String getChannelName()
	{
		return channelName;
	}

	public void setChannelName(final String channelAfterUnpark)
	{
		this.channelName = channelAfterUnpark;
	}

	public String getParkingLotNo()
	{
		return parkingLotNo;
	}

	public void setParkingLotNo(final String parkingLotNo)
	{
		this.parkingLotNo = parkingLotNo;
	}

	public String getCallerId()
	{
		return callerId;
	}

	public void setCallerId(final String callerId)
	{
		this.callerId = callerId;
	}

	public AsteriskChannel getChannel()
	{
		return channel;
	}

	public void setChannel(final AsteriskChannel channel)
	{
		this.channel = channel;
	}

	public String getChannelId()
	{
		return channelId;
	}

	public void setChannelId(final String channelId)
	{
		this.channelId = channelId;
	}
}
